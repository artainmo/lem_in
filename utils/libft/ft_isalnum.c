#include "libft.h"

int	ft_isalnum(int c)
{
	if (c < 123 && c > 96)
		return (1);
	else if (c > 47 && c < 58)
		return (1);
	else if (c < 91 && c > 64)
		return (1);
	else
		return (0);
}
